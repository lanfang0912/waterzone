你不是太敏感：12 種容易造成情感忽視的父母類型對照清單

先說在前面，這份清單不是要你急著責怪誰，也不是要替誰下定論。
它比較像一份對照表，幫你慢慢看見：有些你後來的反應、委屈、過度懂事，可能都不是沒有原因。

你不一定只會對上一種類型。很多人的成長經驗，常常是混合的。
你可以一邊看，一邊感受：哪幾種最像你熟悉的家庭氛圍。

1. 自戀型父母
把孩子當成自己的延伸、門面或成就展示。很在意你有沒有替他爭光，卻不太在意你真正的感受。
→ 孩子長大後容易過度在意別人的評價，習慣討好、證明自己，內在常有「我不夠好」的焦慮。

2. 權威型父母
習慣命令、控制、要求服從。家裡有很多規定，但很少真正對話，也很少允許孩子表達不同意見。
→ 孩子長大後害怕犯錯、害怕衝突、不敢表達需求，容易壓抑，或在關係裡過度順從。

3. 放縱型父母
表面上什麼都不管，很少設界線，也缺乏穩定陪伴與引導。孩子看似自由，實際上常常很沒有安全感。
→ 孩子長大後界線感薄弱，心裡常有一種「沒有人真正接住我」的感覺。

4. 活在憂傷裡的單親父母
父母自己長期困在失落、壓力或未處理的傷痛裡，常常無力顧及孩子的情緒。
→ 孩子長大後很早熟、很懂事，容易把照顧別人當成責任，卻不太會照顧自己。

5. 成癮型父母
情緒和行為容易被酒精、藥物、賭博或其他成癮模式牽動，家庭氛圍常忽冷忽熱。
→ 孩子長大後缺乏安全感，習慣觀察別人的情緒變化，容易焦慮、過度警覺。

6. 憂鬱型父母
長期低能量、消沉，對孩子的需要回應有限，常讓孩子覺得自己被忽略。
→ 孩子長大後容易自責，覺得自己是負擔，不太敢麻煩別人。

7. 工作狂型父母
很努力賺錢，願意提供物質，但長期缺席孩子的情緒世界。東西不缺，陪伴卻很少。
→ 孩子長大後表面獨立，內在卻很孤單，容易把成就和價值綁在一起。

8. 照顧重心長期放在特殊成員身上的父母
家裡有高需求成員，父母大量心力放在那裡，其他孩子容易被忽略。
→ 孩子長大後習慣當最懂事、最不麻煩的人，心裡常有一種「我不重要」的感覺。

9. 成就導向型父母
最重視成績、表現、競爭力。肯定常常跟結果綁在一起，很少單純看見孩子這個人。
→ 孩子長大後容易完美主義，害怕失敗，很難在沒有表現的時候感到有價值。

10. 反社會型父母
缺乏同理心與責任感，可能操控、說謊、推卸責任，把自己的問題投射到孩子身上。
→ 孩子長大後對人缺乏信任，容易混淆愛與控制。

11. 把孩子當情緒配偶或照顧者的父母
讓孩子過早承擔傾聽、安撫、陪伴等角色，把孩子當成情緒支柱。
→ 孩子長大後很會照顧別人，卻不太會照顧自己，容易在關係裡過度付出。

12. 缺乏自覺型父母
不一定有惡意，但對自己的情緒與教養模式缺乏覺察，常常愛得用力，卻沒有真正接住孩子。
→ 孩子長大後心裡常有一種說不上來的空，會懷疑自己是不是太敏感。

